// Parallax effect for floating icons
window.addEventListener('scroll', () => {
    const scrolled = window.pageYOffset;
    const iconContainers = document.querySelectorAll('.icon-container');
    
    iconContainers.forEach((container, index) => {
        const speed = 0.3 + (index * 0.05);
        const yPos = -(scrolled * speed);
        const xPos = Math.sin(scrolled * 0.001 + index) * 10;
        container.style.transform = `translateY(${yPos}px) translateX(${xPos}px)`;
    });
});

// Add interactive effects to icons
document.addEventListener('DOMContentLoaded', () => {
    const iconContainers = document.querySelectorAll('.icon-container');
    
    iconContainers.forEach((container, index) => {
        // Add random rotation on hover
        container.addEventListener('mouseenter', function() {
            this.style.transform += ' rotate(10deg)';
            this.style.animationPlayState = 'paused';
        });
        
        container.addEventListener('mouseleave', function() {
            this.style.transform = this.style.transform.replace(' rotate(10deg)', '');
            this.style.animationPlayState = 'running';
        });
        
        // Add click effect
        container.addEventListener('click', function() {
            this.style.transform += ' scale(1.3)';
            setTimeout(() => {
                this.style.transform = this.style.transform.replace(' scale(1.3)', '');
            }, 200);
        });
    });
});

